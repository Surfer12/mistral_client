- `/adaptiveSettings`: Manage adaptive settings.
- `/cognitiveModels/{cognitiveModelId}`: Manage cognitive models.
- `/patterns`: Recognize usage patterns.
- `/selfMonitoringData`: Monitor system performance.

### Schemas
